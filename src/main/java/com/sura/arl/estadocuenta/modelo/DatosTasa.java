package com.sura.arl.estadocuenta.modelo;

public class DatosTasa {

	private Double tasaCalculada;

	private String centroTrabajo;

	private String centroTrabajoPagador;

	public Double getTasaCalculada() {
		return tasaCalculada;
	}

	public void setTasaCalculada(Double tasaCalculada) {
		this.tasaCalculada = tasaCalculada;
	}

	public String getCentroTrabajo() {
		return centroTrabajo;
	}

	public void setCentroTrabajo(String centroTrabajo) {
		this.centroTrabajo = centroTrabajo;
	}

	public String getCentroTrabajoPagador() {
		return centroTrabajoPagador;
	}

	public void setCentroTrabajoPagador(String centroTrabajoPagador) {
		this.centroTrabajoPagador = centroTrabajoPagador;
	}

}
